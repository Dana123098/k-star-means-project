import math

def calculate_mdl(points, centroids, sse):
    """
    Вычисляет стоимость MDL для текущей кластеризации.
    Чем меньше это число, тем лучше мы подобрали количество кластеров k.
    """
    n = len(points)          # количество всех точек
    d = len(points[0])       # размерность (сколько колонок/признаков)
    k = len(centroids)       # текущее количество кластеров

    # Защита от SSE = 0 (если вдруг точки совпали с центрами идеально)
    if sse <= 0:
        sse = 1e-10

    # 1. L_data: Насколько сильно "разбросаны" данные относительно центров
    # Мы оцениваем дисперсию (sigma^2) и берем логарифм
    variance = sse / (n * d)
    l_data = n * d * math.log(variance)

    # 2. L_model: Штраф за сложность (чтобы алгоритм не плодил лишние кучки)
    # (a) Стоимость хранения координат центров
    l_centroids = k * d * math.log(n)
    
    # (b) Стоимость хранения информации о принадлежности каждой точки к кластеру
    l_assignments = n * math.log(k)

    # Итоговая сумма
    mdl_total = l_data + l_centroids + l_assignments
    
    return mdl_total